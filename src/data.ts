export function getItemsForPart(partIndex: number): AssessmentItem[] {
  return ASSESSMENT_ITEMS.filter(item => item.part === partIndex);
}

export function getPartMaxScore(partIndex: number): number {
  return ASSESSMENT_PARTS.find(p => p.index === partIndex)?.maxScore || 1;
}

export function getPartPassingThreshold(partIndex: number): number {
  const part = ASSESSMENT_PARTS.find(p => p.index === partIndex);
  if (!part) return 0;
  if (partIndex <= 4) {
    return part.maxScore; // S1-S4 require full scores (ต้องผ่านทุกข้อ)
  } else if (partIndex === 5) {
    return 52; // s5 rating threshold (80% of 65)
  } else if (partIndex === 6) {
    return 80; // s6 rating threshold (80% of 100)
  } else if (partIndex === 7) {
    return 66; // s7 rating threshold (80% of 82)
  } else if (partIndex === 8) {
    return 48; // s8 rating threshold (80% of 60)
  }
  return 0;
}
